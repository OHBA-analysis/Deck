function c = jet( n, signed )
%
% c = jet( n=64, signed=false )
%
% Blue-white/black-red colormap.
%   n is the desired number of colors (default: 64).
%   signed can be set to true if caxis is symmetric & centered at 0.
%   

    if nargin < 1, n = 64; end
    if nargin < 2, signed = false; end
    
    method = 'pchip';

    if signed
        C = [ ...
            -1.00, 0.0, 0.0, 1.0; ...
            -0.80, 0.0, 0.3, 0.9; ...
            -0.35, 0.0, 0.9, 0.9; ...
            -0.00, 0.1, 0.2, 0.1; ...
            +0.35, 0.9, 0.9, 0.0; ...
            +0.80, 0.9, 0.3, 0.0; ...
            +1.00, 1.0, 0.0, 0.0  ...
        ];
        x = linspace(-1,1,n)';
    else
        C = [ ...
            -0.00, 0.0, 0.0, 0.2; ...
            +0.20, 0.0, 0.1, 1.0; ...
            +0.35, 0.0, 0.9, 0.9; ...
            +0.50, 0.3, 0.9, 0.3; ...
            +0.65, 0.9, 0.9, 0.0; ...
            +0.90, 1.0, 0.1, 0.0; ...
            +1.00, 0.8, 0.0, 0.0  ...
        ];
        C = [ ...
            -0.0, 0.0, 0.0, 0.2; ...
            +0.2, 0.1, 0.2, 0.9; ...
            +0.5, 0.3, 0.9, 0.2; ...
            +0.6, 0.8, 0.9, 0.0; ...
            +0.8, 0.9, 0.6, 0.0; ...
            +1.0, 1.0, 0.0, 0.0  ...
        ];
        x = linspace(0,1,n)';
    end

    c = interp1( C(:,1), C(:,2:4), x, method );

end
